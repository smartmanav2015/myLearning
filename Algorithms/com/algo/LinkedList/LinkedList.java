package com.algo.LinkedList;

public class LinkedList<T> {
	
	
	private T value;
	
	private T next;



	public T getValue() {
		return value;
	}

	public void setValue(T value) {
		this.value = value;
	}

	public T getNext() {
		return next;
	}

	public void setNext(T next) {
		this.next = next;
	}
	
	

}
